#include "libft.h"

size_t	strlcpy(char *dst, const char *src, size_t size)
{
	unsigned char	*d;
	unsigned char	*s;
	size_t			i;

	d = (unsigned char *)dst;
	s = (unsigned char *)src;
	i = 0;
	while (i < size-1)
	{
		d[i] = s[i];
		i++;
	}
	d[i] = '\0';
}
